# ----------------------------if - elif - else (else - if) --------------------------------

score = 33

if score > 90:
    print("you are a topper")
elif score > 80 and score <= 90:
    print("work little bit harder to become a topper")
elif score > 60 and score <= 80:
    print("you can improve")
else:
    print("poor performance")