# operators of same precedence will follow Left --> Right order

print(5 + 8 - 2)  # 11 --->> 5 + 8 = 13 - 2 = 11
print(5 - 8 + 2)  # -1 --->> 5 - 8 = -3 + 2 = -1


print(5 - (8 + 2)) # -5 --->> 5 - (10) = -5  () has more precedence


